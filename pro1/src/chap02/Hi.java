package chap02;

public class Hi {
	public static void main(String[] args) {
		System.out.println("Hello, Java");
		System.out.println("안녕하세요"); 	// 한줄 주석문
		/* 여러줄 주석문 */
		System.out.println(3);  		// 숫자
		System.out.println("3"); 		 // 문자
		System.out.println("1번\r\n"); 		 
		System.out.print("2번"); 		 	// println 이 아닌 print 입력후 줄바꿈 하지 않음
		System.out.println("\r3번"); 	
		System.out.println("\n\n");
		System.out.print("4번"); 		
		System.out.println("5번"); 		
	}
}

